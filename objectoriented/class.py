# oop :object oriented programming

# class :design pattern/blueprint
# object: real world entity
# reference :to perform operations on object


class Person:

    def helloWorld(self):
        print("hello world")

    def helloWorl(self):
        print("hello worl")


obj1 = Person()
obj2 = Person()
obj2.helloWorld()
obj2.helloWorl()
obj1.helloWorld()
obj1.helloWorl()

# 2 types of variable
# 1 static variable  related to class.. access using class name
# 2 instance variable.. related to methods access using self
